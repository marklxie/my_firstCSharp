using System;
using System.Collections.Generic;
using System.Text;

namespace Math {
	public class Algebra {
		public static double Inverse(double num) {
			return 1 / num;
		}
		public static int Modulo(int a, int b) {
			return a % b;
		}

		public static long Cubed(int a) {
			return a * a * a;
		}
	}
}
